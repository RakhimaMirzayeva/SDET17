public class E4NestedLopps {
    public static void main(String[] args) {


        int y = 1;
        while (y <= 3) {
            int x = 1;
            while (x <= 5) {
                System.out.print("+");
              x++;
            }
            System.out.println();
            y++;
        }
    }
}
