package review4;

public class E2LoopsRecap {
    public static void main(String[] args) {

        for (int i = 1; i < 5; i++) {//curly bracket means code to be repeat
            System.out.println("Aydin");
        }
        for (int j = 2; j < 10; j += 3) {
            System.out.println("Ali");
        }
        System.out.println("Nested Loops");
        for (int g = 1; g < 3; g++) {
            System.out.println("Halit");

            for (int h = 1; h <= 4; h++) {
                System.out.println("Hello World");
//we can put Break; here it will print Halit 1 time and Hello World 1 time each
            }



            }
        }
    }


