package JAVAHomework;

public class Replit94 {
    public static void main(String[] args) {
        String given = "I love Java classes at Syntax";

        // Retrieve and print the first substring
        String substring1 = given.substring(0, 11); // Start at index 0 and end at index 11 (exclusive)
        System.out.println(substring1);

        // Retrieve and print the second substring
        String substring2 = given.substring(12); // Start at index 12 and go until the end
        System.out.println(substring2);
    }
    }

