package HOMEWORK;

public class TwoDArrays {
    public static void main(String[] args) {

        int[][] integers=new int[2][2];//[[0,0],[0,0]]
        integers[0][0]=3; //[[3,0],[0,0]]
        integers[0][1]=5;//[[3,5],[0,0]]
        integers[1][0]=10;//[[3,0], [10,0]]
        integers[1][1]=2;//[[3,5],[10,2]]
        integers[1][2]=7;//[[
        System.out.print(integers[0][0]+" ");
        System.out.print(integers[0][1]+" ");
        System.out.print(integers[1][1]+" ");
        System.out.print(integers[1][0]+" ");
        System.out.println(" ");
        System.out.print(integers[0][0]+" ");
        System.out.print(integers[0][1]+" ");
        System.out.print(integers[1][1]+" ");
        System.out.print(integers[1][0]+" ");
    }
}

