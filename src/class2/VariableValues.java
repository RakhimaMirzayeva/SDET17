package class2;

public class VariableValues {
    public static void main(String[] args) {

        byte cookiePrice=35;

        System.out.println(cookiePrice);
        cookiePrice=37;
        System.out.println(cookiePrice);
        short myFirstCarYear=2014;
        System.out.println(myFirstCarYear);
        myFirstCarYear=2022;
        System.out.println(myFirstCarYear);
        boolean isSunny=true;
        System.out.println(isSunny);
        isSunny=false;
        System.out.println(isSunny);
        double carPrice=123.456;
        System.out.println(carPrice);
        carPrice=123.789;
        System.out.println(carPrice);
        int salary=12000;
        salary=13000;
        System.out.println(salary);
        long distance=11255221255L;
        distance=545544544;
        System.out.println(distance);
        float number1=123.253F;
        number1=24552.2254F;
        System.out.println(number1);
        boolean isRich=false;
        isRich=true;
        System.out.println(isRich);



    }
}
